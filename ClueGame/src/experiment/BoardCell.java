package experiment;

public class BoardCell {
	int row;
	int column;
	public BoardCell(int row, int column) {
		super();
		this.row = row;
		this.column = column;
	}
	public void setRow(int row) {
		this.row = row;
	}
	public void setColumn(int column) {
		this.column = column;
	}
	
	
	
}
